"""Генерация КП: LLM (Grok/Claude/любой из [llm]) персонализирует письмо
под агентство. Fail-open: без ключа/при ошибке — шаблон из config.toml."""

from __future__ import annotations

import json

from fpv import llm

SYSTEM = """Ты пишешь короткие коммерческие письма от имени студии {author}
(Москва) — FPV-съёмка премиальной и загородной недвижимости,
позиционирование «архитектура впечатления». Пиши от «мы».

Главный тезис бренда (можно перефразировать, но сохраняй смысл):
«Фотография показывает, как выглядит дом. FPV одним кадром показывает,
как в нём ощущается жизнь». Покупатель покупает не картинки — ощущение
пространства; FPV-тур даёт его до первого визита.

Суть продукта — непрерывный однокадровый FPV-тур, снятый глазами гостя:
фасад → входишь в дом → гостиная → кухня → вылет на участок → второй
этаж. Без склеек, поэтому зрителю не нужно думать, «где сейчас этот
кадр» — он просто проходит по объекту за пару минут. Так почти никто
не летает: это сложно, и это выделяет объект.

Факты о студии (вплети 1–3 самых уместных, не перечисляй все):
- пилот работает с дронами 8+ лет, из них более двух — специализация
  на премиальной недвижимости, сотрудничает с ведущими агентствами элитки
- сотни снятых объектов, включая самые дорогие интерьеры, — без единого
  инцидента: аккуратная и безопасная работа внутри и снаружи
- до 3–4 объектов за один съёмочный день — цена в пересчёте на объект
  снижается
- готовый ролик — через 1–2 дня после съёмки, полный цикл: съёмка,
  стабилизация, монтаж
- форматы: однокадровые FPV-туры и классические монтажные презентации;
  адаптация под продажу, аренду или девелоперскую подачу

Ценность для агентства (выбери 1–2 ближайших к их профилю):
- покупатель знакомится с объектом удалённо и приезжает на показ уже
  «тёплым» → меньше пустых показов
- объект заметно выделяется среди десятков похожих объявлений
- видео работает везде: сайт, соцсети, прямая отправка клиенту

Цены (упомяни только подходящий формат, со словом «от»):
{prices}

Условия оплаты: {payment} — одной короткой строкой в конце.

Тон: короткий, спокойный, профессиональный. Без громких обещаний, без
комплиментов агентству, без канцелярита и восклицаний. Главный призыв —
посмотреть примеры: {portfolio}

Запрещено:
- обещать рост продаж, выдумывать цифры, кейсы и клиентов
- скидки, торг, криптовалюта
- подтверждать конкретные даты съёмки
- называть модели дронов и камер (слово Blackmagic допустимо)
- факты про агентство, которых нет в справке

Требования: 90–150 слов, 1 конкретная деталь про агентство из справки
(без лести), обращение «Здравствуйте!», подпись:
{author} · архитектура впечатления · {phone}

Ответь СТРОГО одним JSON-объектом, без пояснений до и после:
{{"subject": "тема письма, до 60 знаков", "body": "текст письма с переносами строк"}}"""


def _template(agency: dict, kp_cfg: dict) -> tuple[str, str]:
    subject = kp_cfg["subject"].format(agency=agency["name"])
    body = kp_cfg["template"].format(
        agency=agency["name"], portfolio=kp_cfg["portfolio_url"],
        author=kp_cfg["author_name"], phone=kp_cfg["author_phone"],
        prices=kp_cfg.get("prices", ""), payment=kp_cfg.get("payment", ""))
    return subject, body


def make(agency: dict, site_text: str, cfg: dict, log) -> tuple[str, str]:
    """Возвращает (subject, body)."""
    kp_cfg = cfg["kp"]
    system = SYSTEM.format(portfolio=kp_cfg["portfolio_url"],
                           author=kp_cfg["author_name"],
                           phone=kp_cfg["author_phone"],
                           prices=kp_cfg.get("prices", ""),
                           payment=kp_cfg.get("payment", ""))
    info = (f"Агентство: {agency['name']}\nСайт: {agency.get('site','')}\n"
            f"Заметка: {agency.get('note','')}\n"
            f"Справка с сайта: {site_text or 'нет данных'}")
    txt = llm.chat(system, info, cfg, log)
    if not txt:
        log("kp: LLM недоступен — шаблон")
        return _template(agency, kp_cfg)
    try:
        v = json.loads(txt[txt.find("{"):txt.rfind("}") + 1])
        return str(v["subject"])[:120], str(v["body"])
    except Exception as e:
        log(f"kp: не разобрал ответ LLM ({e}) — шаблон")
        return _template(agency, kp_cfg)
